public class MoveStrategyResult {
    private int x;
    private int y;
    public MoveStrategyResult(){ x = 0; y = 0; }
    public void setX(int x){
        this.x = x;
    }
    public void setY(int y){
        this.y = y;
    }
    public int getX(){
        return this.x;
    }
    public int getY(){
        return this.y;
    }
}
