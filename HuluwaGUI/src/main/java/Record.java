public class Record {

    private String imgName;
    private int x;
    private int y;

    public Record(String imgName, int x, int y) {
        this.imgName = imgName;
        this.x = x;
        this.y = y;
    }

    public String getImgName() {
        return this.imgName;
    }


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }


}
